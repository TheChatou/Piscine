#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	cti;

	i = 0;
	sign = 1;
	cti = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		sign = sign * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		cti *= 10;
		cti += str[i] - '0';
		i++;
	}
	cti *= sign;
	return (cti);
}

