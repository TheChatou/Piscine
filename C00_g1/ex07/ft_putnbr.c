#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write (1, "-2147483648", 11);
	if (nb == -2147483647)
		write (1, "-2147483647", 11);
	if (nb == 2147483647)
		write (1, "2147483647", 10);
	if ((nb < 0) && (nb > -2147483647))
	{
		write (1, "-", 1);
		nb = -nb;
	}
	if ((nb >= 10) && (nb < 2147483647))
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if ((nb != -2147483648) && (nb != 2147483647) && (nb != -2147483647))
	{
		ft_putchar(nb + '0');
	}
}

